package SecondPackage;

public class ClassConstructor {

    private String pole1;
    private Integer pole2;
    private Boolean pole3;


    public ClassConstructor(String pole1, Integer pole2, Boolean pole3){
        this.pole1 = pole1;
        this.pole2 = pole2;
        this.pole3 = pole3;
    }

public String getPole1() {
        return pole1;
}

public Integer getPole2() {
        return  pole2;
}

public Boolean getPole3() {
        return pole3;
}}

